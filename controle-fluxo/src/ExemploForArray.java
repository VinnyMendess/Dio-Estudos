public class ExemploForArray {

    public static void main ( String[] args) {

        String alunos  [] = {"João", "Maria", "Pedro", "Ana", "Carlos"};

        for (int x = 0; x < alunos.length; x++) {

            System.out.println("O aluno no indice x = "+ x + " é " + alunos[x]);
        }
    }}